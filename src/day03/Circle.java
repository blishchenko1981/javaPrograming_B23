package day03;

public class Circle {

    public static void main(String[] args) {
         double pi = 3.14;
         int r = 5;
         double area = r*r*pi;
        System.out.print("Area of our circle is: ");
        System.out.println(area);

        double perimeter = 2*r*pi;
        System.out.print("Perimeter of a circle is: ");
        System.out.println(perimeter);







    }





}
