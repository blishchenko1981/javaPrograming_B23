package day22;

public class DoWhileLoop {
    public static void main(String[] args) {
        int i = 10;
        while (i<5){
            System.out.println("Hello Cybertek");
        }
        System.out.println("------------------------------------");

        do {
            System.out.println("Hello Cybertek");

        }while(i<5);

    }
}
